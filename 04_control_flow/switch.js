const month = 4

switch (month) {
    case "jan":
        console.log("january")
         break;
    case "Feb":
        console.log("Feb")
         break;
    case 3:
        console.log("Mar")
         break;
    case 4:
        console.log("April")
         break;
    case 5:
        console.log("May")
         break;

    default:
        console.log("default case match");
        break;
}


